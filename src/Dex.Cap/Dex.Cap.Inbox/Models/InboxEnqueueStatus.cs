namespace Dex.Cap.Inbox.Models;

/// <summary>
/// Результат приёма сообщения в инбокс.
/// </summary>
public enum InboxEnqueueStatus
{
    /// <summary>
    /// Сообщение принято и будет обработано.
    /// </summary>
    Accepted = 0,

    /// <summary>
    /// Сообщение с такой парой MessageId + ConsumerId уже принято ранее и повторно не сохраняется.
    /// </summary>
    /// <remarks>
    /// Это штатный исход при at-least-once доставке, а не ошибка: источнику следует подтвердить сообщение.
    /// <para>
    /// «Ранее» здесь означает «другой успешной вставкой», но не обязательно другой ДОСТАВКОЙ. Приём не
    /// повторяет вставку сам, поэтому повтор всегда приходит снаружи, и им может оказаться повторный заход
    /// самого вызывающего после того, как предыдущий завершился ошибкой уже ПОСЛЕ вставки: такому вызывающему
    /// <c>Duplicate</c> расскажет про его же строку. На решение это не влияет (сообщение сохранено, источнику
    /// надо подтвердить), но строить на статусе бизнес-логику вида «это сообщение нам уже присылали» нельзя.
    /// </para>
    /// </remarks>
    Duplicate = 1
}