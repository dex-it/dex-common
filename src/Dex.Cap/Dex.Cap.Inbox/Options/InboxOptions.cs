using System;

namespace Dex.Cap.Inbox.Options;

/// <summary>
/// Настройки приёма и обработки сообщений. Проверяются валидатором на старте хоста.
/// </summary>
public class InboxOptions
{
    /// <summary>
    /// Количество попыток обработки сообщения. По исчерпании сообщение переводится в DeadLettered.
    /// Default: 3
    /// </summary>
    public int Retries { get; set; } = 3;

    /// <summary>
    /// Количество сообщений, захватываемых обработчиком за один цикл.
    /// Время обработки ВСЕХ выбранных сообщений отсчитывается с момента их захвата.
    /// Default: 25
    /// </summary>
    /// <remarks>
    /// Задаёт бюджет времени на сообщение вместе с арендой и параллелизмом:
    /// <c>(LockTimeout - 5s) * ConcurrencyLimit / MessagesToProcess</c>. На дефолтах это секунда на
    /// сообщение, чего хватает обработчику с походом в базу или по сети. Партия больше бюджета не ускоряет
    /// разбор: хвост, не успевший начаться до конца аренды, всё равно вернётся в выборку следующим циклом,
    /// а захват на него будет потрачен впустую. Пропускная способность от размера партии не зависит:
    /// обработчик не делает паузу, пока партии приходят полными.
    /// </remarks>
    public int MessagesToProcess { get; set; } = 25;

    /// <summary>
    /// Степень параллелизма обработки. ConcurrencyLimit должен быть меньше либо равен MessagesToProcess.
    /// Default: 1
    /// </summary>
    public int ConcurrencyLimit { get; set; } = 1;

    /// <summary>
    /// Таймаут захвата партии сообщений из хранилища. Задаётся с точностью до секунды, минимум 1 секунда.
    /// Default: 20sec
    /// </summary>
    /// <remarks>
    /// Гранулярность в секундах у таймаута команды, а не у этой настройки: доля секунды при переводе
    /// усекается в ноль, а ноль означает «таймаут не задан», то есть настройка молча потерялась бы целиком.
    /// Валидатор отвергает такое значение на старте.
    /// </remarks>
    public TimeSpan GetFreeMessagesTimeout { get; set; } = TimeSpan.FromSeconds(20);
}