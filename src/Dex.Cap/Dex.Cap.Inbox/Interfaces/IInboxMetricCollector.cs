using System;

namespace Dex.Cap.Inbox.Interfaces;

internal interface IInboxMetricCollector : IInboxStatistic
{
    void IncProcessCount();
    void IncEmptyProcessCount();
    void IncProcessJobCount();
    void IncProcessJobSuccessCount();
    void IncProcessJobFailedCount();
    void IncDeadLetteredCount();
    void IncDuplicateCount();

    /// <summary>
    /// Сообщение даже не начали обрабатывать: аренда истекла, пока дренировалась партия.
    /// </summary>
    /// <remarks>Устойчиво ненулевое значение означает, что LockTimeout мал для MessagesToProcess.</remarks>
    void IncExpiredBeforeStartCount();

    /// <summary>
    /// Аренда потеряна во время обработки: либо она истекла под работающим обработчиком, либо строку успел
    /// забрать другой.
    /// </summary>
    /// <remarks>
    /// Диагностирует ту же причину, что и <see cref="IncExpiredBeforeStartCount"/> (мал LockTimeout для времени
    /// слива всей партии), но вторую её половину: там аренда умерла в очереди на обработку, здесь во время самой
    /// обработки. Попытка в обоих случаях не тратится, поэтому без этого счётчика потеря аренды не была бы видна
    /// вовсе: сообщение молча возвращалось бы в выборку, а ProcessJobCount переставал бы сходиться с суммой
    /// успехов и отказов без объяснимой причины.
    /// </remarks>
    void IncLeaseLostCount();

    void AddProcessJobDuration(TimeSpan duration);
}

/// <summary>
/// Признаки жизни обработчика инбокса. Используется health check'ом.
/// </summary>
public interface IInboxStatistic
{
    /// <summary>
    /// Момент последнего признака жизни обработчика.
    /// </summary>
    /// <remarks>
    /// Признак жизни это состоявшаяся выборка партии или начатая задача, но не завершённая работа: штамп
    /// сообщает, что обработчик жив, а не что он преуспел. Цикл, который начался и завис в обработчике, обязан
    /// ронять health check по <c>Period * 2</c>, а не считаться живым до своего завершения.
    /// <para>
    /// Границы выбраны с двух сторон. Состоявшаяся выборка это уже признак жизни, даже если обрабатывать было
    /// нечего или вся партия оказалась непригодной: иначе health check ронял бы заведомо здоровый обработчик.
    /// А цикл, не доехавший до хранилища, признаком жизни не является вовсе: иначе недоступность БД не
    /// детектировалась бы, ведь заходы на неё продолжались бы ровно с той же частотой.
    /// </para>
    /// <para>
    /// Начатая задача отмечается наравне с циклом, потому что порог health check (<c>Period * 2</c>) не связан с
    /// длительностью цикла. Дренаж партии ограничен арендой, а <see cref="Models.InboxEnvelope.MaxLockTimeout"/>
    /// это сутки, поэтому при настроенной длинной аренде цикл законно идёт дольше порога. На дефолтах (аренда
    /// 30 секунд против порога в минуту) отметки по циклу хватило бы, но опираться на совпадение двух
    /// независимо настраиваемых величин нельзя: без поштучной отметки занятый обработчик уходил бы в Degraded.
    /// </para>
    /// </remarks>
    DateTime GetLastStamp();
}