namespace Dex.Cap.Inbox.Jobs;

/// <summary>
/// Результат захвата партии: задачи к обработке и сколько строк всего захвачено.
/// </summary>
/// <remarks>
/// Число захваченных отделено от числа задач намеренно. Непригодные строки хоронятся ещё в провайдере и в
/// задачи не попадают, но решение о паузе планировщика зависит от полноты ЗАХВАТА, а не от того, сколько строк
/// дошло до обработчика. Иначе одна непригодная строка в полной партии занижала бы результат ниже лимита, и
/// планировщик уходил бы в паузу, хотя очередь не исчерпана.
/// </remarks>
/// <param name="Jobs">Задачи, которые надо обработать (без непригодных строк).</param>
/// <param name="ClaimedCount">Сколько строк захвачено всего, включая уже похороненные непригодные.</param>
internal readonly record struct InboxJobBatch(IInboxLockedJob[] Jobs, int ClaimedCount);